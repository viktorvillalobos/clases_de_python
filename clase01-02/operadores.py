A = 2
B = 4
# Multiplicacion
print('Multiplicacion: ' + str(A * B))

# Division
print('Division: ' + str(A / B))

# Division Entera
print('Division Entera: ' + str(A // B))

# MOD %
print('Residuo: ' + str(A % B))

# AND OR
print("And: " + str(A == B))

# Mayor Menor Que
print(f'Es paja y la variable vale {A} y aqui hay mas texto')

# Mayor Menor Que
print('Es paja y la variable vale {0} y aqui hay mas texto {1}'.format(A, B))
